//Se uma classe que herda de uma abstrata e não implementa os seus métodos, o que ocorre?

//Resposta:

/*
Se uma classe herda de uma classe abstrata e não implementa todos os métodos abstratos, 
ocorrerá um erro de compilação. A subclasse pode ser declarada como abstrata para evitar o erro de compilação mas,
não há obrigação de implementar os métodos abstratos da classe mãe.
*/